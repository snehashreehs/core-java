class Book {

    public static void main(String[] args) {

        String title; // declaration
        System.out.println("I am printing declaration part of title:");
        title = "Java Programming"; // initialization
        System.out.println("I am printing initialization part of title: " + title);

        int bookId; // declaration
        System.out.println("I am printing declaration part of bookId:");
        bookId = 101; // initialization
        System.out.println("I am printing initialization part of bookId: " + bookId);

        long isbnNumber; // declaration
        System.out.println("I am printing declaration part of isbnNumber:");
        isbnNumber = 9876543210L; // initialization
        System.out.println("I am printing initialization part of isbnNumber: " + isbnNumber);

        double price; // declaration
        System.out.println("I am printing declaration part of price:");
        price = 499.50; // initialization
        System.out.println("I am printing initialization part of price: " + price);

        char grade; // declaration
        System.out.println("I am printing declaration part of grade:");
        grade = 'A'; // initialization
        System.out.println("I am printing initialization part of grade: " + grade);

        boolean isAvailable; // declaration
        System.out.println("I am printing declaration part of isAvailable:");
        isAvailable = true; // initialization
        System.out.println("I am printing initialization part of isAvailable: " + isAvailable);

    }
}