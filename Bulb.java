class Bulb {

    public static void main(String[] args) {

        char type; // declaration
        System.out.println("I am printing declaration part of type:");
        type = 'L'; // initialization
        System.out.println("I am printing initialization part of type: " + type);

        String brand; // declaration
        System.out.println("I am printing declaration part of brand:");
        brand = "Philips"; // initialization
        System.out.println("I am printing initialization part of brand: " + brand);

        long serialNumber; // declaration
        System.out.println("I am printing declaration part of serialNumber:");
        serialNumber = 9876543210L; // initialization
        System.out.println("I am printing initialization part of serialNumber: " + serialNumber);

        int bulbId; // declaration
        System.out.println("I am printing declaration part of bulbId:");
        bulbId = 101; // initialization
        System.out.println("I am printing initialization part of bulbId: " + bulbId);

        boolean isLed; // declaration
        System.out.println("I am printing declaration part of isLed:");
        isLed = true; // initialization
        System.out.println("I am printing initialization part of isLed: " + isLed);

        long warrantyPeriod; // declaration
        System.out.println("I am printing declaration part of warrantyPeriod:");
        warrantyPeriod = 24L; // initialization
        System.out.println("I am printing initialization part of warrantyPeriod: " + warrantyPeriod);

    }
}